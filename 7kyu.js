function solve(s) {
  if (s.length <= 1) return true;
  for (let i = 0; i < s.length; i++) {
    const l = s[i];
    const l2 = s[s.length - 1 - i];
    if (l === l2) continue;
    const ind = [l.charCodeAt() + 1, l2.charCodeAt() + 1];
    const ind2 = [l.charCodeAt() - 1, l2.charCodeAt() - 1];
    if (
      ind[0] === ind[1]
      || ind[0] === ind2[1]
      || ind[1] === ind2[0]
      || ind2[0] === ind2[1]
    ) continue;

    return false;
  }
  return true;
}

