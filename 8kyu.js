function arr2bin(arr) {
  let res = 0;
  for (let i = 0; i < arr.length; i++) {
    const e = arr[i];
    if (isNaN(e) && e !== e) return 'NaN';
    if (!e || typeof e !== 'number') continue;
    res += +e;
  }
  return res.toString(2);
}


