function noBoringZeros(n) {
  if (n === 0) return n;

  n = "" + n;
  if (n[n.length - 1] !== '0') return +n;

  for (let i = n.length - 1; i >= 0; i--) {
    if (n[i] === '0') {
      n = n.substring(0, i);
    } else {
      break;
    }
  }
  
  return +n;
}

