function makeUpperCase(s) {
  let res = "";
  for (let i = 0; i < s.length; i++) {
    let ascii = s.charCodeAt(i);
    res += (ascii >= 97 && ascii <= 122) ? String.fromCharCode(ascii & 223) : s[i];
  }
  return res;
}

